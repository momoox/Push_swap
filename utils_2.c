#include "push_swap.h"

int	zero_checker(char *str)
{
	int	i;

	i = 0;
	while (str[i] && !(str[i] >= '0' && str[i] <= '9'))
		i++;
	while (str[i] == '0')
	{
		if (str[i] && (str[i + 1] >= '0' && str[i + 1] <= '9'))
			return (1);
		i++;
	}
	return (0);
}